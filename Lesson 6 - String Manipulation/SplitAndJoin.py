#Splitting string
str = "string in python"

print(str.split(" "))
#Output : ['string', 'in', 'python']

print(str.split("in"))
#Output : ['str', 'g ', ' python']

#Joining string
arr = ["Example", "of", "join"]

print(" ".join(arr))
#Output : Example of join

print("---".join(arr))
#Output : Example---of---join
